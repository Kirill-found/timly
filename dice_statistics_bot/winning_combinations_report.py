"""
–ê–Ω–∞–ª–∏–∑ –¢–û–õ–¨–ö–û –≤—ã–∏–≥—Ä—ã—à–Ω—ã—Ö –∫–æ–º–±–∏–Ω–∞—Ü–∏–π
"""

# –î–∞–Ω–Ω—ã–µ –∏–∑ –æ—Ç—á–µ—Ç–∞
statistics = {
    1: 21,   # BAR BAR BAR
    22: 16,  # üçãüçãüçã
    43: 14,  # üçáüçáüçá
    64: 15   # 7Ô∏è‚É£7Ô∏è‚É£7Ô∏è‚É£
}

# –í—ã–ø–ª–∞—Ç—ã
payouts = {
    1: 100,   # BAR BAR BAR
    22: 50,   # üçãüçãüçã
    43: 30,   # üçáüçáüçá
    64: 200   # 7Ô∏è‚É£7Ô∏è‚É£7Ô∏è‚É£
}

# –°–∏–º–≤–æ–ª—ã
symbols = {
    1: "BAR BAR BAR",
    22: "üçãüçãüçã",
    43: "üçáüçáüçá",
    64: "7Ô∏è‚É£7Ô∏è‚É£7Ô∏è‚É£"
}

total_rolls = 1000
total_wins = sum(statistics.values())
win_rate = (total_wins / total_rolls) * 100

print("\n" + "="*70)
print("    –ê–ù–ê–õ–ò–ó –í–´–ò–ì–†–´–®–ù–´–• –ö–û–ú–ë–ò–ù–ê–¶–ò–ô (1000 –ë–†–û–°–ö–û–í)")
print("="*70 + "\n")

print("–í–´–ò–ì–†–´–®–ù–´–ï –ö–û–ú–ë–ò–ù–ê–¶–ò–ò:")
print("-" * 70)

total_payout = 0

for value in [64, 1, 22, 43]:  # –°–æ—Ä—Ç–∏—Ä—É–µ–º –ø–æ –≤—ã–ø–ª–∞—Ç–µ (–æ—Ç –±–æ–ª—å—à–µ–π –∫ –º–µ–Ω—å—à–µ–π)
    count = statistics[value]
    payout = payouts[value]
    symbol = symbols[value]
    probability = (count / total_rolls) * 100
    total_value = count * payout
    total_payout += total_value

    print(f"{symbol:15s} | –í—ã–ø–∞–ª–æ: {count:3d} —Ä–∞–∑ | –í–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç—å: {probability:5.2f}% | –í—ã–ø–ª–∞—Ç–∞: {payout:3d} –∑–≤–µ–∑–¥")
    print(f"{'':15s} | –í—Å–µ–≥–æ –≤—ã–ø–ª–∞—á–µ–Ω–æ: {total_value:,} –∑–≤–µ–∑–¥")
    print("-" * 70)

print(f"\n{'–ò–¢–û–ì–û:':15s}")
print(f"{'':15s} –í—Å–µ–≥–æ –≤—ã–∏–≥—Ä—ã—à–µ–π: {total_wins} –∏–∑ {total_rolls} ({win_rate:.2f}%)")
print(f"{'':15s} –í—Å–µ–≥–æ –≤—ã–ø–ª–∞—á–µ–Ω–æ: {total_payout:,} –∑–≤–µ–∑–¥")
print(f"{'':15s} –ü—Ä–æ–∏–≥—Ä—ã—à–µ–π: {total_rolls - total_wins} ({100-win_rate:.2f}%)")

# RTP —Ä–∞—Å—á–µ—Ç
average_bet = 35  # –∏–∑ .env MESSAGE_COST
total_bet = total_rolls * average_bet
rtp = (total_payout / total_bet) * 100

print(f"\n" + "="*70)
print("    RTP –ê–ù–ê–õ–ò–ó")
print("="*70)
print(f"–°—Ä–µ–¥–Ω—è—è —Å—Ç–∞–≤–∫–∞: {average_bet} –∑–≤–µ–∑–¥")
print(f"–í—Å–µ–≥–æ –ø–æ—Å—Ç–∞–≤–ª–µ–Ω–æ (1000 –∏–≥—Ä): {total_bet:,} –∑–≤–µ–∑–¥")
print(f"–í—Å–µ–≥–æ –≤—ã–ø–ª–∞—á–µ–Ω–æ: {total_payout:,} –∑–≤–µ–∑–¥")
print(f"RTP (Return to Player): {rtp:.2f}%")
print(f"–ü—Ä–µ–∏–º—É—â–µ—Å—Ç–≤–æ –∫–∞–∑–∏–Ω–æ: {100-rtp:.2f}%")

print("\n" + "="*70)
print("    –†–ï–ö–û–ú–ï–ù–î–ê–¶–ò–ò")
print("="*70)

if rtp > 100:
    print("‚ö†Ô∏è  RTP > 100% - –∏–≥—Ä–æ–∫–∏ –≤ –ø–ª—é—Å–µ! –ù—É–∂–Ω–æ —Å–Ω–∏–∑–∏—Ç—å –≤—ã–ø–ª–∞—Ç—ã –∏–ª–∏ —É–≤–µ–ª–∏—á–∏—Ç—å —Å—Ç–∞–≤–∫—É")
elif rtp > 95:
    print("‚úÖ RTP –≤—ã—Å–æ–∫–∏–π (>95%) - –≤—ã–≥–æ–¥–Ω–æ –∏–≥—Ä–æ–∫–∞–º, –Ω–æ –¥–æ–ø—É—Å—Ç–∏–º–æ")
elif rtp > 85:
    print("‚úÖ RTP –æ–ø—Ç–∏–º–∞–ª—å–Ω—ã–π (85-95%) - –±–∞–ª–∞–Ω—Å –º–µ–∂–¥—É –∏–≥—Ä–æ–∫–æ–º –∏ –¥–æ–º–æ–º")
elif rtp > 75:
    print("‚ö†Ô∏è  RTP –Ω–∏–∑–∫–∏–π (<85%) - –∏–≥—Ä–æ–∫–∏ –±—ã—Å—Ç—Ä–æ –ø–æ—Ç–µ—Ä—è—é—Ç –∏–Ω—Ç–µ—Ä–µ—Å")
else:
    print("‚ùå RTP —Å–ª–∏—à–∫–æ–º –Ω–∏–∑–∫–∏–π (<75%) - –Ω–∏–∫—Ç–æ –Ω–µ –±—É–¥–µ—Ç –∏–≥—Ä–∞—Ç—å")

print("\n" + "="*70 + "\n")
